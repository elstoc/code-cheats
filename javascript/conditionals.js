/* if...then...else */

// single line if syntax
if(condition) statement;

// single line if..then..else
if(condition1)
    statement1;
else if(condition2)
    statement2;
else
    statement3;

// with code blocks
if(condition1) {
    statements1;
} else if(condition2) {
    statements2;
} else {
    statements3;
}